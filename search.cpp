/******************************************************************************
* FUNCTION EndOfProgramBanner
*******************************************************************************/

int linearSearch( const vector<string>& list,
					   const string& key );

{
/***************************************************************************
* CONSTANTS
****************************************************************************/


/***************************************************************************
* VARIABLES
****************************************************************************/

	for(int index = 0; index < int(list.size()); index += 1)
	{
		if (list[index].find(key) != string::npos)
		{
			return index;
		}
	}

}




